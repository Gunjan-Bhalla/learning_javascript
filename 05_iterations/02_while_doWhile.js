// while and do while loop

let index = 0
while(index <= 10){
    console.log(`Vlaue of index is ${index}`);
    index = index + 2;
}

let myArray = ['flash', 'superman','batman']
let arr = 0;
while(arr < myArray.length){
    console.log(`Vlaue is ${myArray[arr]}`);
    arr++;
}


// ==========do while=====================

let score = 1;
do{
    console.log(`Score is ${score}`);
    score++;
} while(score <= 10);